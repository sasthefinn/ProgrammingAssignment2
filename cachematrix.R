## makeCacheMatrix creates a cached matrix object that caches the inverse of the input matrix
## cacheSolve returns the inverse of the cached matrix, recalculating if necessary

## Store input matrix, and create cache object that includes methods to get and set the data

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinv <- function(solve) m <<- solve
  getinv <- function() m
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}


## Retrieve inverse from cache object, calculating the inverse if it isn't set

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getinv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinv(m)
  m
}
